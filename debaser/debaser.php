<?_title("Meaning")?>
<u>Webster's Revised Unabridged Dictionary (1913)</u><br>
<b>Debase</b> [De*base"], v. t. [imp. & p. p. Debased; p. pr. & vb. n. Debasing.] [Pref. de- + base. See Base, a., and cf. Abase.] To reduce from a higher
to a lower state or grade of worth, dignity, purity, station, etc.; to degrade; to lower; to deteriorate; to abase; as, to debase the character by crime; to
debase the mind by frivolity; to debase style by vulgar words.<br>
<i>Syn:</i> To abase; degrade.<br>
<b>Debaser</b> [De*bas"er], n. One who, or that which, debases.<br>
<br>
<u>WordNet (r) 1.5</u><br>
<b>debase</b> v<br>
1: corrupt morally [syn: corrupt, pervert, demoralize, debauch, profane, vitiate, deprave, misdirect] <br>
2: lower in value; of metals [syn: alloy]<br>
3: add water to a mixture to dilute it [syn: adulterate, water down, dilate]<br>
